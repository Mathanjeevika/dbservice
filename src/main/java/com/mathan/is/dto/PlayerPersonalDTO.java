package com.mathan.is.dto;

public class PlayerPersonalDTO {

	private String firstName;
	private String lastName;
	private String middleName;
	private String placeOfBirth;
	private String fatherName;
	private String motherName;
	private String dateOfBirth;

	private AddressDTO address;

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public String getMiddleName() {
		return middleName;
	}

	public void setMiddleName(String middleName) {
		this.middleName = middleName;
	}

	public String getPlaceOfBirth() {
		return placeOfBirth;
	}

	public void setPlaceOfBirth(String placeOfBirth) {
		this.placeOfBirth = placeOfBirth;
	}

	public String getFatherName() {
		return fatherName;
	}

	public void setFatherName(String fatherName) {
		this.fatherName = fatherName;
	}

	public String getMotherName() {
		return motherName;
	}

	public void setMotherName(String motherName) {
		this.motherName = motherName;
	}

	public String getDateOfBirth() {
		return dateOfBirth;
	}

	public void setDateOfBirth(String dateOfBirth) {
		this.dateOfBirth = dateOfBirth;
	}

	public AddressDTO getAddress() {
		return address;
	}

	public void setAddress(AddressDTO address) {
		this.address = address;
	}

	@Override
	public String toString() {
		return "PlayerPersonalDTO [firstName=" + firstName + ", lastName=" + lastName + ", middleName=" + middleName
				+ ", placeOfBirth=" + placeOfBirth + ", fatherName=" + fatherName + ", motherName=" + motherName
				+ ", dateOfBirth=" + dateOfBirth + ", address=" + address + "]";
	}
	
	
}
